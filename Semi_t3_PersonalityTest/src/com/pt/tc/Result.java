package com.pt.tc;

public class Result {
	int e, i, n, s , t, f, j, p;
	
	
	
	public Result() {
	}

	public Result(int e, int i, int n, int s, int t, int f, int j, int p) {
		super();
		this.e = e;
		this.i = i;
		this.n = n;
		this.s = s;
		this.t = t;
		this.f = f;
		this.j = j;
		this.p = p;
	}

	public int getE() {
		return e;
	}

	public void setE(int e) {
		this.e = e;
	}

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public int getN() {
		return n;
	}

	public void setN(int n) {
		this.n = n;
	}

	public int getS() {
		return s;
	}

	public void setS(int s) {
		this.s = s;
	}

	public int getT() {
		return t;
	}

	public void setT(int t) {
		this.t = t;
	}

	public int getF() {
		return f;
	}

	public void setF(int f) {
		this.f = f;
	}

	public int getJ() {
		return j;
	}

	public void setJ(int j) {
		this.j = j;
	}

	public int getP() {
		return p;
	}

	public void setP(int p) {
		this.p = p;
	}

	
}
