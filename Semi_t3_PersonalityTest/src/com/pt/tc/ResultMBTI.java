package com.pt.tc;

public class ResultMBTI {
	String EIResult, NSResult, TFResult, JPResult, MBTIResult;
	
	public ResultMBTI() {
	}

	public ResultMBTI(String eIResult, String nSResult, String tFResult, String jPResult, String mBTIResult) {
		super();
		EIResult = eIResult;
		NSResult = nSResult;
		TFResult = tFResult;
		JPResult = jPResult;
		MBTIResult = mBTIResult;
	}

	public String getEIResult() {
		return EIResult;
	}

	public void setEIResult(String eIResult) {
		EIResult = eIResult;
	}

	public String getNSResult() {
		return NSResult;
	}

	public void setNSResult(String nSResult) {
		NSResult = nSResult;
	}

	public String getTFResult() {
		return TFResult;
	}

	public void setTFResult(String tFResult) {
		TFResult = tFResult;
	}

	public String getJPResult() {
		return JPResult;
	}

	public void setJPResult(String jPResult) {
		JPResult = jPResult;
	}

	public String getMBTIResult() {
		return MBTIResult;
	}

	public void setMBTIResult(String mBTIResult) {
		MBTIResult = mBTIResult;
	}

	
	
}